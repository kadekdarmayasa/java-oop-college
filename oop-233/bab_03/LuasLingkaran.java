package bab_03;

public class LuasLingkaran {
    public static void main(String[] args) {
        // Deklarasi konstanta
        final double PI = 3.14;

        // Deklarasi Variabel
        double r, luas;

        // Inisialisasi nilai untuk variabel
        r = 15;


        // Proses perhitungan
        luas = PI * r * r;

        // Output hasil
        System.out.println("Luas lingkaran dengan r = " + r + " adalah " + luas);
    }
}
