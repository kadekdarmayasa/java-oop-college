package bab_06;

public class Contoh8ParameterArray {
    public static void main(String[] args) {
        System.out.println("Argumen yang diberikan : ");
        // Menampilkan argumen yang diberikan
        for (int i = 0; i < args.length; i++) {
            System.out.println(args[i]);
        }
    }
}
