package bab_05;

public class PerulanganCountedWhile {
    public static void main(String[] args) {
        int jumlahPerulangan = 0;

        // Perulangan WHILE-DO
        while(jumlahPerulangan <= 5) {
            // Perintah yang diulang
            System.out.println("Perulangan ke-" + jumlahPerulangan);
            // Increment variabel counter
            jumlahPerulangan++;
        }
    }
}
